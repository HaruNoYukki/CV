function typeLogo() {
    const logoText = "< AG />";
    const logoElement = document.getElementById("typed-logo");
    let index = 0;

    // Limpiamos el contenido por si se recarga la página
    logoElement.innerHTML = "";

    function type() {
        if (index < logoText.length) {
            // Añade el carácter actual
            logoElement.innerHTML += logoText.charAt(index);
            index++;
            
            // Tiempo aleatorio entre teclas para simular que un humano escribe (entre 100ms y 250ms)
            const typingSpeed = Math.random() * 150 + 100;
            setTimeout(type, typingSpeed); 
        }
    }

    // Retraso de medio segundo antes de empezar a escribir al abrir la página
    setTimeout(type, 500);
}

